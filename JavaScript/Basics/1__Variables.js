// Variables are the storage containers for values

// Variables declared with let keyword can't be declared again and it have block scope

// Variables declared with const keyword can't be redeclared or re-assigned and have block scope

// Naming Pattern:

//     Variable names can contain letters, digits, underscores. names can start with letters or underscore but can't with numbers

//     Variable name are case-sensitive and avoid using keywords as variable names

// We don't use var to declare varibales because it allows redeclaring, reassigning, hoisting and have global scope


let person_name = "Kishore";
const designation = "Software developer";
var age = 25;

