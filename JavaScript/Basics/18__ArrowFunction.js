// Arrow functions, introduced in ECMAScript 6 (ES6), provide more readable way to write functions in JavaScript. Arrow functions are especially useful for writing short, simple functions.


// Arrow function with no parameters
const sayHello = () => "Hello, world!";

// Arrow function with one parameter
const double = (x) => x * 2;

// Arrow function with multiple parameters
const add = (a, b) => a + b;

// Arrow function with a block statement (useful for longer functions)
const greet = (name) => {
    return `Hello, ${name}!`;
}
